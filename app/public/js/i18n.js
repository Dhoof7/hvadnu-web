function getCurrentLang() {
  return localStorage.getItem('lang') || 'da';
}

function applyLang(lang) {
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const val = TRANSLATIONS[lang]?.[key];
    if (val === undefined) return;
    if (el.tagName === 'TEXTAREA' || (el.tagName === 'INPUT' && el.type === 'text')) {
      el.placeholder = val;
    } else {
      el.innerHTML = val;
    }
  });
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang === 'da' ? '🇬🇧 EN' : '🇩🇰 DA';
}

function t(key) {
  return TRANSLATIONS[getCurrentLang()]?.[key] || TRANSLATIONS['en']?.[key] || key;
}

document.addEventListener('DOMContentLoaded', () => applyLang(getCurrentLang()));
